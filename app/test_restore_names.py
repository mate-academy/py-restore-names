import pytest
from app.restore_names import restore_names


@pytest.fixture()
def set_template() -> list[dict]:
    incorrect_users = [
        {
            "first_name": None,
            "last_name": "Holy",
            "full_name": "Jack Holy",
        },
        {
            "last_name": "Adams",
            "full_name": "Mike Adams",
        },
    ]
    return incorrect_users


def test_restore_name(set_template: list[dict]) -> None:
    users = set_template
    restore_names(users)
    assert users == [
        {
            "first_name": "Jack",
            "last_name": "Holy",
            "full_name": "Jack Holy",
        },
        {
            "first_name": "Mike",
            "last_name": "Adams",
            "full_name": "Mike Adams",
        },
    ]
